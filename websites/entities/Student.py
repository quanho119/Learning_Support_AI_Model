class Student:
    def __init__(self, id, name):
        self.id = id
        self.name = name

    # Getter and Setter for id
    def get_id(self):
        return self.id

    def set_id(self, id):
        self.id = id

    # Getter and Setter for name
    def get_name(self):
        return self.name

    def set_name(self, name):
        self.name = name
